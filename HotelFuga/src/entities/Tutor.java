
package entities;

public class Tutor {
    
    private String cpfTutor;
    private String nameTutor;
    private String pet; //race and name
    private String telephoneTutor;
    private String emergencyTelephoneTutor;
    private String cep;
    private String address;
    private String numberAddress;

    /**
     * @return the cpfTutor
     */
    public String getCpfTutor() {
        return cpfTutor;
    }

    /**
     * @param cpfTutor the cpfTutor to set
     */
    public void setCpfTutor(String cpfTutor) {
        this.cpfTutor = cpfTutor;
    }

    /**
     * @return the nameTutor
     */
    public String getNameTutor() {
        return nameTutor;
    }

    /**
     * @param nameTutor the nameTutor to set
     */
    public void setNameTutor(String nameTutor) {
        this.nameTutor = nameTutor;
    }

    /**
     * @return the pet
     */
    public String getPet() {
        return pet;
    }

    /**
     * @param pet the pet to set
     */
    public void setPet(String pet) {
        this.pet = pet;
    }

    /**
     * @return the telephoneTutor
     */
    public String getTelephoneTutor() {
        return telephoneTutor;
    }

    /**
     * @param telephoneTutor the telephoneTutor to set
     */
    public void setTelephoneTutor(String telephoneTutor) {
        this.telephoneTutor = telephoneTutor;
    }

    /**
     * @return the emergencyTelephoneTutor
     */
    public String getEmergencyTelephoneTutor() {
        return emergencyTelephoneTutor;
    }

    /**
     * @param emergencyTelephoneTutor the emergencyTelephoneTutor to set
     */
    public void setEmergencyTelephoneTutor(String emergencyTelephoneTutor) {
        this.emergencyTelephoneTutor = emergencyTelephoneTutor;
    }

    /**
     * @return the cep
     */
    public String getCep() {
        return cep;
    }

    /**
     * @param cep the cep to set
     */
    public void setCep(String cep) {
        this.cep = cep;
    }

    /**
     * @return the address
     */
    public String getAddress() {
        return address;
    }

    /**
     * @param address the address to set
     */
    public void setAddress(String address) {
        this.address = address;
    }

    /**
     * @return the numberAddress
     */
    public String getNumberAddress() {
        return numberAddress;
    }

    /**
     * @param numberAddress the numberAddress to set
     */
    public void setNumberAddress(String numberAddress) {
        this.numberAddress = numberAddress;
    }
    
}
